    def solver(self,n,A,x,k,i,dp):
        if(i>=n):
            return i;
        if(k==0 and x<=0):
            return i;

        if(dp[x][k]!=-1):
            return dp[x][k];

        if(k==0 and x>0):
            dp[x][k]=self.solver(n,A,x-A[i],k,i+1,dp)
            return dp[x][k]

        if(k>0 and x<=0):
            k-=1
            ans=self.solver(n,A,x,k,i+1,dp)
            k+=1;
            dp[x][k]=ans
            return dp[x][k];

        k-=1;
        op2 = self.solver(n,A,x,k,i+1,dp)
        k+=1 
        op1 =  self.solver(n,A,x-A[i],k,i+1,dp);
        
        dp[x][k]=max(op1,op2)
        return dp[x][k]  








